    public MaskFormatter pegarFormatterDate(){
        MaskFormatter mformat = null;
        try{
            mformat=new MaskFormatter("##/##/####");
            mformat.setPlaceholderCharacter('_');
        }
        catch(ParseException e){
            
        }
        return mformat;
    }
    
    
    
    
    
    public void inserirValoresFuncionario(Funcionario f){
        txtid.setText(f.getId().toString());
        inserirValoresCliente(f.getClienteId());
        txtcargo.setText(f.getCargoId().getNome());
        cbcargo.setModel(new DefaultComboBoxModel(new Object[]{f.getCargoId()}));
        txtusuario.setText(f.getUsuarioId().getNomeUsuario());
        txtsenha.setText(f.getUsuarioId().getSenha());
        txtemail.setText(f.getUsuarioId().getEmail());
        txtsalario.setText(f.getSalario().toString());
        txtnome.requestFocus();
        modoalteracao(true);
    }
    
    public void inserirValoresCliente(Cliente c){
        txtnome.setText(c.getNome());
        txtdtnascimento.setText(formatador.format(c.getDataNasc()));
        txttelefone.setText(c.getTelefone());
        txtendereco.setText(c.getEndereco());
        txtbairro.setText(c.getBairro());
        txtcidade.setText(c.getCidade());
        txtrg.setText(c.getRg());
        txtcpf.setText(c.getCpf());
        txtcnpj.setText(c.getCnpj());
        txtcargo.requestFocus();
        modoalteracao(false);
    }
    
    public void modoalteracao(Boolean b){
        txtusuario.setEnabled(!b);
        txtsenha.setEnabled(!b);
        txtemail.setEnabled(!b);
        btsalvar.setEnabled(!b);
        btalterar.setEnabled(b);
    }
    
