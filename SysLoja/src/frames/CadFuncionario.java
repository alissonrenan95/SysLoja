    private void txtpesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpesquisaKeyReleased
        // TODO add your handling code here:
        if(!txtpesquisa.getText().isEmpty()){
            List<Cliente> funcionarios;
            if(rbnome.isSelected()){
                funcionarios=new Cliente().listarNomeCpfPorNome(txtpesquisa.getText());
                tbcliente.setModel(getModelo(funcionarios));
            }
            else{
                if(txtpesquisa.getText().length()==11){
                    funcionarios=new Cliente().listarNomeCpfPorCpf(txtpesquisa.getText());
                    tbcliente.setModel(getModelo(funcionarios));
                }
            }
        }
    }//GEN-LAST:event_txtpesquisaKeyReleased

    private void tbclienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbclienteMouseClicked
        // TODO add your handling code here:
        Integer linha=tbcliente.getSelectedRow();
        List<Funcionario> funcionarios=new Funcionario().listarPorCpf(tbcliente.getValueAt(linha, 1).toString());
        try{
            inserirValoresFuncionario(funcionarios.get(0));
        }
        catch(Exception e){
            limparCampos();
            Cliente cliente=new Cliente().listarPorCpf(tbcliente.getValueAt(linha, 1).toString()).get(0);
            inserirValoresCliente(cliente);
            
            
        }
    }//GEN-LAST:event_tbclienteMouseClicked

