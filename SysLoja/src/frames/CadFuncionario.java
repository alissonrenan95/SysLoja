    public DefaultTableModel getModelo(List<Cliente> lista){
        Vector<String> headers=new Vector<String>();
        headers.add("Nome");
        headers.add("CPF");
        Vector<Object> vetorvalores=new Vector<Object>();
        for(Cliente c:lista){
            Vector vetor=new Vector();
            vetor.add(c.getNome());
            vetor.add(c.getCpf());
            vetorvalores.add(vetor);
        }
        return new DefaultTableModel(vetorvalores, headers);
    }
    
    public Boolean verificarCampos(){
        try {
            formatador.parse(txtdtnascimento.getText());
        } catch (ParseException ex) {
            return false;
        }
        try{
            Double salario=Double.parseDouble(txtsalario.getText());
        }
        catch(Exception e){
            return false;
        }
        return !txtnome.getText().isEmpty()&&
               !txttelefone.getText().isEmpty()&&
               !txtendereco.getText().isEmpty()&&
               !txtbairro.getText().isEmpty()&&
               !txtcidade.getText().isEmpty()&&
               !cbcargo.getSelectedItem().toString().isEmpty()&&
               !txtusuario.getText().isEmpty()&&
               !txtsenha.getText().isEmpty()&&
               !txtemail.getText().isEmpty();
    }
    
    
    
    public Funcionario pegarDadosCampos(){
        if(verificarCampos()){
            Integer id = null;
            Date dt = null;
            try {
                id=Integer.parseInt(txtid.getText());
            } 
            catch (Exception ex) {
                //pode ter tentado converter o id em integer no campo vazio(caso de salvar que não precisa de id).
            }
            try {
                dt=formatador.parse(txtdtnascimento.getText());
                
                Cliente cliente=new Cliente(null, txtnome.getText(),
                               dt, txttelefone.getText(), txtendereco.getText(), 
                               txtbairro.getText(), txtcidade.getText(),
                               txtrg.getText(), txtcpf.getText(), txtcnpj.getText());
                Cargo cargo=(Cargo)cbcargo.getSelectedItem();
                Usuario usuario=new Usuario(null, txtusuario.getText(), txtsenha.getText(), txtemail.getText());
                Funcionario funcionario=new Funcionario(id, Double.parseDouble(txtsalario.getText()), cargo, cliente, usuario);
                return funcionario;
                
            } 
            catch (ParseException ex) {
                //campo não pode ser vazio, se der erro retorna null.
            }
        }
        return null;
    }
    
    
