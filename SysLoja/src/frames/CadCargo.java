    public void inserirValoresCargo(Cargo c){
        txtid.setText(c.getId().toString());
        txtnome.setText(c.getNome());
        txtdescricao.setText(c.getDescricao());
        cbnivelacesso.setSelectedIndex(c.getNivelAcesso()-1);
        modoalteracao(true);
    }
    public void modoalteracao(Boolean b){
        btsalvar.setEnabled(!b);
        btalterar.setEnabled(b);
    }
    
    public void limparCampos(){
        rbnome.setSelected(true);
        txtpesquisa.setText("");
        txtid.setText("");
        txtnome.setText("");
        txtdescricao.setText("");
        modoalteracao(false);
        txtpesquisa.requestFocus();
    }
    
    public DefaultTableModel getModelo(List<Cargo> lista){
        Vector<String> headers=new Vector<String>();
        headers.add("Id");
        headers.add("Nome");
        Vector<Object> vetorvalores=new Vector<Object>();
        for(Cargo c:lista){
            Vector vetor=new Vector();
            vetor.add(c.getId());
            vetor.add(c.getNome());
            vetorvalores.add(vetor);
        }
        return new DefaultTableModel(vetorvalores, headers);
    }
    
    public Boolean verificarCampos(){
        return !txtnome.getText().isEmpty() &&
               !txtdescricao.getText().isEmpty() &&
               !cbnivelacesso.getSelectedItem().toString().isEmpty();
    }
    
    public Cargo pegarDadosCampos(){
        if(verificarCampos()){
            Integer id = null;
            try {
                id=Integer.parseInt(txtid.getText());
            } 
            catch (NumberFormatException ex) {
                //caracteres invalidos ou campo vazio
            }
            Integer nivelacesso=Integer.parseInt(cbnivelacesso.getSelectedItem().toString());
            return new Cargo(id, txtnome.getText(), txtdescricao.getText(), nivelacesso);
        }
        return null;
    }
    
    
    
