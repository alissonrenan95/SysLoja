    private void txtpesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpesquisaKeyReleased
        // TODO add your handling code here:
        if(!txtpesquisa.getText().isEmpty()){
            List<Cargo> cargos;
            if(rbnome.isSelected()){
                cargos=new Cargo().listarPorNome(txtpesquisa.getText());
                tbcargo.setModel(getModelo(cargos));
            }
            else{
                cargos=new Cargo().listarPorId(Integer.parseInt(txtpesquisa.getText()));
                tbcargo.setModel(getModelo(cargos));
            }
        }
    }//GEN-LAST:event_txtpesquisaKeyReleased

    private void tbcargoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbcargoMouseClicked
        // TODO add your handling code here:
        Integer linha=tbcargo.getSelectedRow();
        List<Cargo> cargos=new Cargo().listarPorId(Integer.parseInt(tbcargo.getValueAt(linha, 0).toString()));
        if(cargos.size()>0){
            inserirValoresCargo(cargos.get(0));
        }

    }//GEN-LAST:event_tbcargoMouseClicked

