    private void txtpesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpesquisaKeyReleased
        // TODO add your handling code here:
        if(!txtpesquisa.getText().isEmpty()){
            List<Usuario> usuarios;
            if(rbnome.isSelected()){
                usuarios=new Usuario().listarIdNomeEmailPorNome(txtpesquisa.getText());
                tbusuario.setModel(getModelo(usuarios));
            }
            else{
                usuarios=new Usuario().listarIdNomeEmailPorEmail(txtpesquisa.getText());
                tbusuario.setModel(getModelo(usuarios));
            }
        }
    }//GEN-LAST:event_txtpesquisaKeyReleased

    private void tbusuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbusuarioMouseClicked
        // TODO add your handling code here:
        Integer linha=tbusuario.getSelectedRow();
        List<Usuario> usuarios=new Usuario().listarPorId(Integer.parseInt(tbusuario.getValueAt(linha, 0).toString()));
        if(usuarios.size()>0){
            inserirValoresUsuario(usuarios.get(0));
        }
    }//GEN-LAST:event_tbusuarioMouseClicked
    
    public void inserirValoresUsuario(Usuario u){
        txtid.setText(u.getId().toString());
        txtnome.setText(u.getNomeUsuario());
        txtsenha.setText(u.getSenha());
        txtemail.setText(u.getEmail());
        modoalteracao(true);
    }
    public void modoalteracao(Boolean b){
        btsalvar.setEnabled(!b);
        btalterar.setEnabled(b);
    }
    
    public void limparCampos(){
        rbnome.setSelected(true);
        txtpesquisa.setText("");
        txtid.setText("");
        txtnome.setText("");
        txtsenha.setText("");
        txtemail.setText("");
        modoalteracao(false);
        txtpesquisa.requestFocus();
    }
    
    public DefaultTableModel getModelo(List<Usuario> lista){
        Vector<String> headers=new Vector<String>();
        headers.add("Id");
        headers.add("Nome");
        headers.add("Email");
        Vector<Object> vetorvalores=new Vector<Object>();
        for(Usuario u:lista){
            Vector vetor=new Vector();
            vetor.add(u.getId());
            vetor.add(u.getNomeUsuario());
            vetor.add(u.getEmail());
            vetorvalores.add(vetor);
        }
        return new DefaultTableModel(vetorvalores, headers);
    }
    
    public Boolean verificarCampos(){
        return !txtnome.getText().isEmpty() &&
               !txtsenha.getText().isEmpty() &&
               !txtemail.getText().isEmpty();
    }
    
    public Usuario pegarDadosCampos(){
        if(verificarCampos()){
            Integer id = null;
            try {
                id=Integer.parseInt(txtid.getText());
            } 
            catch (NumberFormatException ex) {
                //caracteres invalidos ou campo vazio
            }
            return new Usuario(id, txtnome.getText(), txtsenha.getText(), txtemail.getText());
        }
        return null;
    }
    
    
    
