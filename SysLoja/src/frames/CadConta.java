    private void txtpesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpesquisaKeyReleased
        // TODO add your handling code here:
        if(!txtpesquisa.getText().isEmpty()){
            List<Conta> contas;
            if(rbdescricao.isSelected()){
                contas=new Conta().listarIdDescricaoDataPorDescricao(txtpesquisa.getText());
                tbconta.setModel(getModelo(contas));
            }
            else{
                Integer id=null;
                try{
                    id=Integer.parseInt(txtpesquisa.getText());
                    contas=new Conta().listarIdDescricaoDataPorId(id);
                    tbconta.setModel(getModelo(contas));
                }
                catch(Exception e){
                    JOptionPane.showMessageDialog(rootPane, "Certifique-se de que digitou somente numeros inteiros \nsem espaço e sem qualquer caracter que não seja número","Caracteres invalidos",JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_txtpesquisaKeyReleased

    private void tbcontaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbcontaMouseClicked
        // TODO add your handling code here:
        Integer linha=tbconta.getSelectedRow();
        List<Conta> contas=new Conta().listarPorId(Integer.parseInt(tbconta.getValueAt(linha, 0).toString()));
        if(contas.size()>0){
            inserirValoresProduto(contas.get(0));
        }
    }//GEN-LAST:event_tbcontaMouseClicked

    public void inserirValoresProduto(Conta c){
        txtid.setText(Integer.toString(c.getId()));
        txtdescricao.setText(c.getDescricao());
        txtvalorparcela.setText(Double.toString(c.getValorParcela()));
        txtnumparcelas.setText(Integer.toString(c.getNumParcelas()));
        txtdtcadastro.setText(formatador.format(c.getDataCadastro()));
        txtdtvencimento.setText(formatador.format(c.getDataVencimento()));
        modoalteracao(true);
    }
    public void modoalteracao(Boolean b){
        btsalvar.setEnabled(!b);
        btalterar.setEnabled(b);
    }
    
    public void limparCampos(){
        rbdescricao.setSelected(true);
        txtpesquisa.setText("");
        txtid.setText("");
        txtdescricao.setText("");
        txtvalorparcela.setText("");
        txtnumparcelas.setText("");
        txtdtcadastro.setText("");
        txtdtvencimento.setText("");
        modoalteracao(false);
        txtpesquisa.requestFocus();
    }
    
    public DefaultTableModel getModelo(List<Conta> lista){
        Vector<String> headers=new Vector<String>();
        headers.add("ID");
        headers.add("Descrição");
        headers.add("Data Vencimento");
        Vector<Object> vetorvalores=new Vector<Object>();
        for(Conta c:lista){
            Vector vetor=new Vector();
            vetor.add(c.getId());
            vetor.add(c.getDescricao());
            vetor.add(formatador.format(c.getDataVencimento()));
            vetorvalores.add(vetor);
        }
        return new DefaultTableModel(vetorvalores, headers);
    }
    
    public Boolean verificarCampos(){
        return !txtdescricao.getText().isEmpty()&&
               !txtvalorparcela.getText().isEmpty()&&
               !txtnumparcelas.getText().isEmpty()&&
                txtdtcadastro.getText().length()==10 &&
                txtdtvencimento.getText().length()==10;
    }
    
    public Conta pegarDadosCampos(){
        if(verificarCampos()){
            Integer id = null;
            Date dtcadastro = null;
            Date dtvencimento = null;
            Double valorparcela = null;
            Integer numparcelas = null;
            
            try {
                id=Integer.parseInt(txtid.getText());
            } 
            catch (Exception e) {
                //caracteres invalidos ou campo vazio
            }
            try {
                dtcadastro=formatador.parse(txtdtcadastro.getText());
                dtvencimento=formatador.parse(txtdtvencimento.getText());
                valorparcela=Double.parseDouble(txtvalorparcela.getText());
                numparcelas=Integer.parseInt(txtnumparcelas.getText());
                return new Conta(id, txtdescricao.getText(), valorparcela, numparcelas, dtcadastro, dtvencimento);
            } catch (ParseException ex) {
                
            }
        }
        return null;
    }
    
