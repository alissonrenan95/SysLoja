    public MaskFormatter pegarFormatterDate(){
        MaskFormatter mformat = null;
        try{
            mformat=new MaskFormatter("##/##/####");
            mformat.setPlaceholderCharacter('_');
        }
        catch(ParseException e){
            
        }
        return mformat;
    }
    
    
    
    
    
    public void inserirValoresCliente(Cliente c){
        txtid.setText(c.getId().toString());
        txtnome.setText(c.getNome());
        txtdtnascimento.setText(formatador.format(c.getDataNasc()));
        txttelefone.setText(c.getTelefone());
        txtendereco.setText(c.getEndereco());
        txtbairro.setText(c.getBairro());
        txtcidade.setText(c.getCidade());
        txtrg.setText(c.getRg());
        txtcpf.setText(c.getCpf());
        txtcnpj.setText(c.getCnpj());
        modoalteracao(true);
    }
