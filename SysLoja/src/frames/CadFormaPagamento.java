    public void inserirValoresFormaPagamento(FormaPagamento fp){
        txtid.setText(fp.getId().toString());
        rbcartao.setSelected(fp.getCartao());
        if(fp.getTipoCartao()){
            rbcredito.setSelected(true);
        }
        else if(!fp.getTipoCartao()){
            rbdebito.setSelected(false);
        }
        txtnumparcelas.setText(Integer.toString(fp.getNumParcelas()));
        txttaxajuros.setText(Double.toString(fp.getTaxaJuros()));
        modoalteracao(true);
    }
    public void modoalteracao(Boolean b){
        btsalvar.setEnabled(!b);
        btalterar.setEnabled(b);
    }
    
    public void limparCampos(){
        txtid.setText("");
        rbcartao.setSelected(true);
        rbcredito.setSelected(true);
        txtnumparcelas.setText("");
        txttaxajuros.setText("");
        modoalteracao(false);
        tbformapag.setModel(getModelo(new FormaPagamento().listar()));
    }
    
    public DefaultTableModel getModelo(List<FormaPagamento> lista){
        Vector<String> headers=new Vector<String>();
        headers.add("Id");
        headers.add("Forma");
        headers.add("Tipo");
        headers.add("Parcelas");
        headers.add("Taxa Juros");
        
        Vector<Object> vetorvalores=new Vector<Object>();
        for(FormaPagamento fp:lista){
            Vector vetor=new Vector();
            vetor.add(fp.getId());
            vetor.add((fp.getCartao())?"Cartão":"Dinheiro");
            vetor.add((fp.getCartao())?(fp.getTipoCartao())?"Credito":"Debito":"Dinheiro");
            vetor.add(fp.getNumParcelas());
            vetor.add(fp.getTaxaJuros());
            vetorvalores.add(vetor);
        }
        return new DefaultTableModel(vetorvalores, headers);
    }
    
    public Boolean verificarCampos(){
        Double taxajuros;
        try{
            taxajuros=Double.parseDouble(txttaxajuros.getText());
        }
        catch(Exception e){
            return null;
        }
        return !txtnumparcelas.getText().isEmpty() &&
               !txttaxajuros.getText().isEmpty();
    }
    
    public FormaPagamento pegarDadosCampos(){
        if(verificarCampos()){
            Integer id = null;
            try {
                id=Integer.parseInt(txtid.getText());
            } 
            catch (Exception ex) {
                //caracteres invalidos ou campo vazio
            }
            return new FormaPagamento(id, rbcartao.isSelected(), rbcredito.isSelected(), Integer.parseInt(txtnumparcelas.getText()), Double.parseDouble(txttaxajuros.getText()));
            //return new FormaPagamento(id, rbcartao.isSelected(), (rbcredito.isSelected())?true:(rbdebito.isSelected())?false:null, Integer.parseInt(txtnumparcelas.getText()), Double.parseDouble(txttaxajuros.getText()));
        }
        return null;
    }
    
    
