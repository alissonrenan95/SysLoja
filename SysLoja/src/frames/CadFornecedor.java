    public void inserirValoresFornecedor(Fornecedor f){
        txtid.setText(f.getId().toString());
        txtnome.setText(f.getNome());
        txttelefone.setText(f.getTelefone());
        txtendereco.setText(f.getEndereco());
        txtbairro.setText(f.getBairro());
        txtcidade.setText(f.getCidade());
        cbestado.setSelectedItem(f.getEstado());
        txtcnpj.setText(f.getCnpj());
        modoalteracao(true);
    }
    public void modoalteracao(Boolean b){
        btsalvar.setEnabled(!b);
        btalterar.setEnabled(b);
    }
    
    public void limparCampos(){
        rbnome.setSelected(true);
        txtpesquisa.setText("");
        txtid.setText("");
        txtnome.setText("");
        txttelefone.setText("");
        txtendereco.setText("");
        txtbairro.setText("");
        txtcidade.setText("");
        cbestado.setSelectedIndex(5);
        txtcnpj.setText("");
        modoalteracao(false);
        txtpesquisa.requestFocus();
    }
    
    public DefaultTableModel getModelo(List<Fornecedor> lista){
        Vector<String> headers=new Vector<String>();
        headers.add("Nome");
        headers.add("CNPJ");
        Vector<Object> vetorvalores=new Vector<Object>();
        for(Fornecedor f:lista){
            Vector vetor=new Vector();
            vetor.add(f.getNome());
            vetor.add(f.getCnpj());
            vetorvalores.add(vetor);
        }
        return new DefaultTableModel(vetorvalores, headers);
    }
    
    public Boolean verificarCampos(){
        return !txtnome.getText().isEmpty()&&
               !txttelefone.getText().isEmpty()&&
               !txtendereco.getText().isEmpty()&&
               !txtbairro.getText().isEmpty()&&
               !txtcidade.getText().isEmpty()&&
               !cbestado.getSelectedItem().toString().isEmpty()&&
               !txtcnpj.getText().isEmpty();
    }
    
    
    
    public Fornecedor pegarDadosCampos(){
        if(verificarCampos()){
            Integer id = null;
            try {
                id=Integer.parseInt(txtid.getText());
            } 
            catch (Exception ex) {
                //pode ter tentado converter o id em integer no campo vazio(caso de salvar que não precisa de id).
            }
            return new Fornecedor(id, txtnome.getText(), txttelefone.getText(), 
                                    txtendereco.getText(), txtbairro.getText(), 
                                    txtcidade.getText(), cbestado.getSelectedItem().toString(), txtcnpj.getText());
            
        }
        return null;
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
