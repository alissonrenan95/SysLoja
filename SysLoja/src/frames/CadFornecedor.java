    private void txtpesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpesquisaKeyReleased
        // TODO add your handling code here:
        if(!txtpesquisa.getText().isEmpty()){
            List<Fornecedor> fornecedores;
            if(rbnome.isSelected()){
                fornecedores=new Fornecedor().listarNomeCnpjPorNome(txtpesquisa.getText());
            }
            else{
                fornecedores=new Fornecedor().listarNomeCnpjPorCnpj(txtpesquisa.getText());
            }
            if(fornecedores!=null){
                tbfornecedor.setModel(getModelo(fornecedores));
            }
        }
    }//GEN-LAST:event_txtpesquisaKeyReleased

    private void tbfornecedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbfornecedorMouseClicked
        // TODO add your handling code here:
        Integer linha=tbfornecedor.getSelectedRow();
        System.out.println(tbfornecedor.getValueAt(linha, 1).toString());
        List<Fornecedor> fornecedores=new Fornecedor().listarPorCnpj(tbfornecedor.getValueAt(linha, 1).toString());
        if(fornecedores.size()>0){
            inserirValoresFornecedor(fornecedores.get(0));
        }
    }//GEN-LAST:event_tbfornecedorMouseClicked

    private void btsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsalvarActionPerformed
        // TODO add your handling code here:
        Fornecedor f=pegarDadosCampos();
        if(f!=null){
            if(f.salvar()){
                JOptionPane.showMessageDialog(rootPane, "Fornecedor salvo com sucesso", "Operação efetuada", JOptionPane.INFORMATION_MESSAGE);
                limparCampos();
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Verifique se esse fornecedor já está cadastrado no sistema", "Operação não efetuada", JOptionPane.ERROR_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Verifique se os campos foram preenchidos corretamente", "Campos inválidos", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btsalvarActionPerformed

    private void btalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btalterarActionPerformed
        // TODO add your handling code here:
        Fornecedor f=pegarDadosCampos();
        if(f!=null){
            if(f.alterar()){
                JOptionPane.showMessageDialog(rootPane, "Fornecedor alterado com sucesso", "Operação efetuada", JOptionPane.INFORMATION_MESSAGE);
                limparCampos();
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Verifique se esse fornecedor já está cadastrado no sistema", "Operação não efetuada", JOptionPane.ERROR_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Verifique se os campos foram preenchidos corretamente", "Campos inválidos", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btalterarActionPerformed

