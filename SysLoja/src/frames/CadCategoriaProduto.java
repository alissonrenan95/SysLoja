    public void inserirValoresCategoria(CategoriaProduto cp){
        txtid.setText(cp.getId().toString());
        txtnome.setText(cp.getNome());
        modoalteracao(true);
    }
    public void modoalteracao(Boolean b){
        btsalvar.setEnabled(!b);
        btalterar.setEnabled(b);
    }
    
    public void limparCampos(){
        rbnome.setSelected(true);
        txtpesquisa.setText("");
        txtid.setText("");
        txtnome.setText("");
        modoalteracao(false);
        txtpesquisa.requestFocus();
        tbcategoria.setModel(getModelo(new CategoriaProduto().listarPorNome("")));
    }
    
    public DefaultTableModel getModelo(List<CategoriaProduto> lista){
        Vector<String> headers=new Vector<String>();
        headers.add("ID");
        headers.add("Nome");
        Vector<Object> vetorvalores=new Vector<Object>();
        for(CategoriaProduto cp:lista){
            Vector vetor=new Vector();
            vetor.add(cp.getId());
            vetor.add(cp.getNome());
            vetorvalores.add(vetor);
        }
        return new DefaultTableModel(vetorvalores, headers);
    }
    
    public Boolean verificarCampos(){
        return !txtnome.getText().isEmpty();
    }
    
    public CategoriaProduto pegarDadosCampos(){
        if(verificarCampos()){
            Integer id = null;
            try {
                id=Integer.parseInt(txtid.getText());
            } catch (Exception ex) {
                //caracteres invalidos ou campo vazio
            }
            return new CategoriaProduto(id, txtnome.getText());
        }
        return null;
    }
    
    
    
    
