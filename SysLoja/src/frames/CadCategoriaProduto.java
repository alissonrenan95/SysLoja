    private void txtpesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpesquisaKeyReleased
        // TODO add your handling code here:
        if(!txtpesquisa.getText().isEmpty()){
            List<CategoriaProduto> categorias = null;
            if(rbnome.isSelected()){
                categorias=new CategoriaProduto().listarPorNome(txtpesquisa.getText());
                
            }
            else{
                try{
                    categorias=new CategoriaProduto().listarPorId(Integer.parseInt(txtpesquisa.getText()));
                }
                catch(Exception e){
                    
                }
            }
            if(categorias!=null){
                tbcategoria.setModel(getModelo(categorias));
            }
        }
    }//GEN-LAST:event_txtpesquisaKeyReleased

